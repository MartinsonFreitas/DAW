	<p>
	De maneira simples e prática, o que é CRUD?
	</p>
	<p>
	No meio empresarial um CRUD é basicamente uma tela de cadastro, com operações em bancos de dados.
	
	Mas não é bem assim, a ideia da sigla surgiu a partir das 4 operações básicas de um banco de dados, Insert, Select, Update e Delete.
	</p>
	<p>
	Então a sigla é um acrônimo, de quatro operações básicas, são elas:
	</p>
	<ul>	
		<li>C: Create – Criar um novo registro.</li>
		<li>R: Read – Ler um registro, ou um conjunto de registros.</li>
		<li>U: Update – Atualizar um registro.</li>
		<li>D: Delete – Excluir um registro.</li>
	</ul>
	
	<p>
	E é isso que iremos demonstrar aqui nesse pequeno projeto... Vamos lá!
