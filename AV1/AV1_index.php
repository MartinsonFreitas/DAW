<p>
O Sr. Water Falls precisa de um sistema de jogo corporativo, para treinar seus gestores em situações difíceis. O jogo deverá gerenciar situações de perguntas e respostas (decisões) encadeadas.
</p>
<p>
O game é composto por vários desafios e cada desafio tem um objetivo específico, como por exemplo, gerenciar o andamento de um projeto, resolver um problema administrativo, contratar um novo funcionário, conceder um empréstimo e outros.
</p>
<p>
Neste primeiro momento será desenvolvido somente o cadastro Usuários, Perguntas e Respostas.
</p>
<p>
Criar as funcionalidades de Criar Pergunta, alterar Pergunta, listar todas Pergunta, listar uma Pergunta e excluir Pergunta.
O Pergunta terá Texto Pergunta, Quantidade de Pontos (1 a 100) e grau de dificuldade (fácil, média, difícil e muito difícil).
</p>
<p>
Inicialmente usaremos arquivos texto(txt) para salvar as perguntas. As funcionalidades de Perguntas devem estar disponíveis por tela. O código deverá ser em PHP.
</p>
<p>
Então deverá ser criado:
</p>

<ul>
	<li>1. Criar Pergunta</li>
	<li>2. Alterar Pergunta</li>
	<li>3. Listar todas Perguntas</li>
	<li>4. Listar um Pergunta</li>
	<li>5. Excluir Pergunta</li>
</ul>	

